#include "../inc/wolf_3d.h"

void		image_clear(t_wolf_3d *w)
{
	int		x;
	int		j;

	x = 0;
	while (x < WIDTH)
	{
		j = 0;
		while (j < HEIGHT)
		{
			w->data[(WIDTH * j) + x] = 0;
			j++;
		}
		x++;
	}
}
